def tr(lang, key, **fmt):
    """Übersetzt 'key' in 'lang'. Fällt auf Englisch und dann auf 'key' zurück."""
    table = _T.get(lang, _T[DEFAULT_LANG])
    text = table.get(key)
    if text is None:
        text = _T[DEFAULT_LANG].get(key, key)
    if fmt:
        try:
            return text.format(**fmt)
        except (KeyError, IndexError, ValueError):
            return text
    return text
